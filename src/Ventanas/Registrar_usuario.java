    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        String rut= txtrut.getText();
        String clave=txtclave.getText();
        String nombre=txtnombre.getText();
        String apellido=txtapellido.getText();
        String fono=txtfono.getText();
        String direccion=txtdireccion.getText();
        String tipo=String.valueOf(cargo.getSelectedItem()) ;
        String comentario=txtcomentario.getText();
        
        int type = 1;
        
        if(tipo.equals("Empleado")){
            
           type=1;
        }
        if(tipo.equals("Administrador")){
            
            type=2;
        }
        
        Controlador_registrar_usuario u = new Controlador_registrar_usuario();
        u.Registrar_usuario(rut, clave, nombre, apellido, fono, direccion, type, comentario);
        
    }//GEN-LAST:event_guardarActionPerformed

