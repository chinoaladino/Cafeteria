    public void Cargar_ventana() {
        String rut = txtrut.getText();
        Controlador_editar_usuario v = new Controlador_editar_usuario();
        v.Ver(rut);

        txtclave.setText(v.Ver(rut).getClave());
        txtnom.setText(v.Ver(rut).getNombre());
        txtape.setText(v.Ver(rut).getApellido());
        txtcoment.setText(v.Ver(rut).getComentario());
        txtdic.setText(v.Ver(rut).getDireccion());
        txtfono.setText(String.valueOf(v.Ver(rut).getFono()));

        if (v.Ver(rut).getTipo() == 1) {
            tipo.setSelectedIndex(0);
        } else {
            tipo.setSelectedIndex(1);
        }
    }
